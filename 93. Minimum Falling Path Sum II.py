def solve(A):
    dp=[[1000 for i in range(len(A))] for j in range(len(A))]
    for i in range(len(A)):
        dp[0][i]=A[0][i]
    for i in range(1,len(A)):
        for j in range(len(A)):
            if j>0 and j<len(A)-1:
                dp[i][j]=min(min(dp[i-1][:j])+A[i][j],min(dp[i-1][j+1:])+A[i][j])
            elif j==0:
                dp[i][j]=min(dp[i-1][1:])+A[i][j]
            elif j==len(A)-1:
                dp[i][j]=min(dp[i-1][:j])+A[i][j]
    return min(dp[-1])

